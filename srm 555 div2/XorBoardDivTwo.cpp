int f(int row, int col, vector <string> v){
	for(int i=0, n=(int)v[row].size(); i<n; ++i)v[row][i] = v[row][i] == '0'? '1':'0';
	for(int i=0, n=(int)v.size(); i<n; ++i)v[i][col] = v[i][col] == '0'? '1':'0';

	int count =0;
	for(int i=0, n=(int)v.size(); i<n; ++i){
		for(int j=0, m=v[i].size(); j<m; ++j){
			if(v[i][j] == '1')++count;
		}
	}
	return count;
}

class XorBoardDivTwo {
public:
	int theMax( vector <string> board ) {
		int best = 0;
		for(int i=0, n=board.size(); i<n; ++i){
			for(int j=0, m=board[i].size(); j<m; ++j){
				int next = f(i,j,board);
				best = max(best, next);
			}
		}
		return best;
	}
};

